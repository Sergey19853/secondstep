public class Example01 {
    public static void main(String[] args) {
        double x;
        double y;

        double sum;
        double difference;
        double product;
        double quotient;

        x = 10;
        y = 20;

        sum = y + x;
        difference = y + x;
        product = y * x;
        quotient = y / x;

        System.out.println("sum = " + sum);
        System.out.println("difference = " + difference);
        System.out.println("product = " + product);
        System.out.println("quotient = " + quotient);
    }
}
