public class Example09 {
    public static void main(String[] args) {
        double a;
        double b;
        double c;
        double d;

        double z;

        a = 1;
        b = 2;
        c = 3;
        d = 4;

        z = a / c * (b / d) - (a * b - c) / (c / d);
        System.out.println("z = " + z);
    }
}
